            % rR fStorage
            if (par.opt_rR & par.opt_fStorage)
                kk = kk + 1;
                C2P_dxtmp = C2Px(:,par.pindx.lfStorage);
                tmp = [-(1-sigC-gamma)*RR_rR*(G*C2P_dxtmp); ...
                    Z ; ...
                    Z ; ...
                    (1-sigC-gamma)*RR_rR*(G*C2P_dxtmp); ...
